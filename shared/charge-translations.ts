export function translateChargeName(name: string): string | undefined {
  return chargeNameTranslations[name];
}

export function translateDescription(description: string): string | undefined {
  for (const [englishBase, spanishBase] of Object.entries(descriptionBaseTranslations)) {
    if (description.includes(englishBase)) {
      const stateMatch = description.match(/ under (.+) law$/);
      if (stateMatch) {
        return `${spanishBase} bajo la ley de ${stateMatch[1]}`;
      }
      return spanishBase;
    }
  }
  return undefined;
}
